from fpdf import FPDF
from datetime import datetime
import io
import pandas as pd
import numpy as np

class PatientReport(FPDF):
    def header(self):
        # Arial bold 15
        self.set_font('Helvetica', 'B', 20)
        # Move to the right
        self.cell(80)
        # Title
        self.set_text_color(102, 126, 234) # Primary color theme
        self.cell(30, 10, 'Patient Risk Report', 0, 0, 'C')
        # Line break
        self.ln(20)

    def footer(self):
        # Position at 1.5 cm from bottom
        self.set_y(-15)
        # Arial italic 8
        self.set_font('Helvetica', 'I', 8)
        self.set_text_color(128)
        # Page number
        self.cell(0, 10, f'Page {self.page_no()} | AI Diagnostics - CONFIDENTIAL', 0, 0, 'C')

def generate_patient_report(prediction_text, confidence, proba, feature_names, input_df, current_shap, insights, threshold):
    """
    Generates a professional PDF report from the analysis results.
    """
    pdf = PatientReport()
    pdf.add_page()
    
    # Metadata
    pdf.set_font('Helvetica', 'B', 12)
    pdf.set_text_color(0)
    pdf.cell(0, 10, f"Analysis Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", ln=True)
    pdf.cell(0, 10, f"System Version: 2.2 (Clinical Decision Control)", ln=True)
    pdf.cell(0, 10, f"Decision Threshold: {threshold:.2f} (User Defined)", ln=True)
    pdf.ln(5)
    
    # Main Result
    pdf.set_font('Helvetica', 'B', 16)
    pdf.set_fill_color(240, 240, 240)
    pdf.cell(0, 15, "1. Diagnostic Conclusion", ln=True, fill=True)
    pdf.ln(5)
    
    res_color = (255, 107, 107) if prediction_text == "Malignant" else (81, 207, 102)
    pdf.set_font('Helvetica', 'B', 24)
    pdf.set_text_color(*res_color)
    pdf.cell(0, 20, f"RESULT: {prediction_text.upper()}", ln=True, align='C')
    
    pdf.set_font('Helvetica', '', 14)
    pdf.set_text_color(50)
    pdf.cell(0, 10, f"AI Confidence Score: {confidence:.1%}", ln=True, align='C')
    pdf.ln(5)

    # Rule-Based Insights Section
    pdf.set_font('Helvetica', 'B', 14)
    pdf.set_text_color(0)
    pdf.cell(0, 10, "Clinical Observations:", ln=True)
    pdf.set_font('Helvetica', '', 10)
    pdf.set_text_color(80)
    for insight in insights:
        pdf.multi_cell(0, 6, f"* {insight['feature']}: {insight['insight']}")
    pdf.ln(10)
    
    # Feature Importance Table
    pdf.set_font('Helvetica', 'B', 16)
    pdf.set_text_color(0)
    pdf.cell(0, 15, "2. Key Feature Contributions (SHAP Analysis)", ln=True, fill=True)
    pdf.ln(5)
    pdf.set_font('Helvetica', 'I', 10)
    pdf.cell(0, 8, "This section identifies which tumor characteristics most influenced the diagnosis.", ln=True)
    pdf.ln(5)
    
    # Prepare SHAP data for table
    if current_shap is not None:
        shap_values_flat = np.array(current_shap).flatten()
    else:
        shap_values_flat = np.zeros(len(feature_names))
        
    # Align lengths
    min_len = min(len(feature_names), len(shap_values_flat))
    aligned_features = feature_names[:min_len]
    aligned_shap = shap_values_flat[:min_len]

    importance_df = pd.DataFrame({
        'Feature': [f.replace('_', ' ').title() for f in aligned_features],
        'Influence': aligned_shap
    })
    importance_df['Abs_Influence'] = importance_df['Influence'].abs()
    top_features = importance_df.sort_values('Abs_Influence', ascending=False).head(10)
    
    # Table Header
    pdf.set_font('Helvetica', 'B', 12)
    pdf.set_fill_color(200, 210, 255)
    pdf.cell(90, 10, "Tumor Characteristic", 1, 0, 'C', fill=True)
    pdf.cell(40, 10, "Influence Level", 1, 0, 'C', fill=True)
    pdf.cell(60, 10, "Direction", 1, 1, 'C', fill=True)
    
    # Table Content
    pdf.set_font('Helvetica', '', 11)
    for _, row in top_features.iterrows():
        pdf.cell(90, 10, f" {row['Feature']}", 1)
        pdf.cell(40, 10, f" {row['Abs_Influence']:.4f}", 1, 0, 'C')
        
        direction = "Toward Malignant" if row['Influence'] > 0 else "Toward Benign"
        if row['Influence'] > 0:
            pdf.set_text_color(200, 0, 0)
        else:
            pdf.set_text_color(0, 150, 0)
        pdf.cell(60, 10, f" {direction}", 1, 1, 'C')
        pdf.set_text_color(0)
        
    pdf.ln(15)
    
    # Disclaimer
    pdf.set_y(-30)
    pdf.set_font('Helvetica', 'B', 8)
    pdf.set_text_color(150, 0, 0)
    pdf.multi_cell(0, 5, "DISCLAIMER: This report is generated by an artificial intelligence system for research and educational purposes only. It should not be used as a primary diagnostic tool. All medical results must be validated by qualified healthcare professionals.", align='C')
    
    # Return as bytes for Streamlit
    try:
        # Check if we are using fpdf2 (has 'buffer' or 'output' returns bytes)
        import fpdf
        is_fpdf2 = hasattr(fpdf, 'FPDF_VERSION') and fpdf.__version__.startswith('2')
        
        if is_fpdf2:
            out = pdf.output()
        else:
            # Legacy PyFPDF (1.7.2) - MUST use dest='S' to get a string and avoid stdout
            out = pdf.output(dest='S')
    except Exception:
        # Final fallback - try string-return mode
        try:
            out = pdf.output(dest='S')
        except:
            out = pdf.output()
    
    if isinstance(out, (bytearray, bytes)):
        return bytes(out)
    elif isinstance(out, str):
        return out.encode('latin-1')
    return bytes() # Fallback for absolute failure
